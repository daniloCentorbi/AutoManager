package it.dvel.tirocinio.exception;

public class ProprietarioException extends Exception {
	private static final long serialVersionUID = -6916542236758350662L;

	public ProprietarioException() {
		super();
	}

	public ProprietarioException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ProprietarioException(String arg0) {
		super(arg0);
	}

	public ProprietarioException(Throwable arg0) {
		super(arg0);
	}
}