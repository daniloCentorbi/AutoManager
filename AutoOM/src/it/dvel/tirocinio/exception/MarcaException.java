package it.dvel.tirocinio.exception;

public class MarcaException extends Exception {
	private static final long serialVersionUID = -26425663940065878L;

	public MarcaException() {
		super();
	}

	public MarcaException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public MarcaException(String arg0) {
		super(arg0);
	}

	public MarcaException(Throwable arg0) {
		super(arg0);
	}
}